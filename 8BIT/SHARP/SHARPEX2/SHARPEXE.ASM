         mov       ah,48h
         int       21h                      ; vytvoen datovho bloku
         jc        Chyba2                   ; nedostatek pamti
         mov       ds:[DatSegm],ax          ; adresa datovho bloku

; ------ zatek jmna souboru

         mov       si,81h
         mov       cl,ds:[si-1]
         mov       ch,0
         cld
Start1:  jcxz      chyba
         mov       dx,si
         lodsb
         dec       cx
         cmp       al," "
         je        Start1
         cmp       al,9
         je        Start1
         cmp       al,13
         jne       Start2

; ------ chyba - nen nic zadno

Chyba:   mov       dx,offset HlpTxt
Chyba2:  mov       ah,9
         int       21h
         int       20h

; ------ oznaen konce jmna souboru

Start2:  jcxz      Start3
         lodsb
         dec       cx
         cmp       al," "
         ja        Start2
         dec       si
Start3:  mov       byte ptr ds:[si],0

; ------ oteven souboru

         mov       ax,3d00h
         int       21h                      ; oteven souboru pro ten
         jc        Chyba                    ; chybn zadn souboru
         mov       bx,ax                    ; identifiktor souboru

; ------ naten hlaviky souboru

         mov       cx,offset(HlavaEnd-Hlava) ; dlka hlaviky
         mov       dx,offset Hlava          ; zatek hlaviky
         mov       ah,3fh
         int       21h                      ; naten hlaviky souboru

; ------ naten souboru do pamti

         push      ds
         mov       ds,ds:[DatSegm]          ; datov segment
         mov       cx,0ffffh                ; velikost dat
         xor       dx,dx                    ; adresa k naten dat
         mov       ah,3fh
         int       21h                      ; naten 1. sti souboru do pamti
         pop       ds
         mov       ds:[DatNum],ax           ; poet bajt

; ------ uzaven souboru

         pushf
         mov       ah,3eh
         int       21h                      ; uzaven souboru
         popf
         jc        Chyba                    ; chyba ten

; ------ hlen o vysln souboru

         mov       dx,offset TextVys
         mov       ah,9
         int       21h

; ------ zobrazen jmna souboru

         mov       cx,17                    ; max. dlka textu
         mov       si,offset HlavaNam
Start6:  mov       dl,ds:[si]
         inc       si
         cmp       dl,13
         je        Start62
         mov       ah,2
