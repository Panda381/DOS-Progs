; -----------------------------------------------------------------------------
;        vysln hlaviky nebo dat
; -----------------------------------------------------------------------------
; VSTUP: BH=dlka synchronizace 1
;        BL=dlka synchronizace 2
;        CX=dlka bloku dat
;        DI=dlka vodnho tnu
;        ES:SI=poten adresa dat
;        DS=datov segment
; VSTUP:CY=peruen programu
; -----------------------------------------------------------------------------

SaveHD   PROC      NEAR

; ------ schova registr

         push      ax
         push      dx
         call      InitPort                 ; instalace a inicializace port

; ------ kontroln souet dat (do registru DX)

         call      KontrS                   ; kontroln souet hlaviky souboru

; ------ vysln vodu ped zpisem

         call      SaveUvod                 ; vysln vodnho tnu a synchron.
         jc        SaveHD9                  ; peruen programu

; ------ vysln obsahu dat

         call      SavBlok                  ; vysln obsahu dat
         jc        SaveHD9                  ; peruen programu

; ------ vysln kontrolnho soutu

         mov       al,dh                    ; vy bajt kontrolnho soutu
         call      SavByte                  ; vysln vyho bajtu kontr.soutu
         mov       al,dl                    ; ni bajt kontrolnho soutu
         call      SavByte                  ; vysln niho bajtu kontr.soutu

; ------ vysln STOP bitu "1"

         stc                                ; pznak bitu "1"
         call      SavBit                   ; vysln stop bitu "1"

; ------ nvrat registr

SaveHD9: pushf
         call      DeInit                   ; nvrat port a peruen
         popf
         pop       dx
         pop       ax
         ret

SaveHD   ENDP

; -----------------------------------------------------------------------------
;        kontroln souet bloku dat
; -----------------------------------------------------------------------------
; VSTUP: ES:SI=adresa bloku dat
;        CX=poet bajt dat
;        DS=datov segment
; VSTUP:DX=kontroln souet dat
; -----------------------------------------------------------------------------

KontrS   PROC      NEAR

; ------ schova registr

         push      ax
         push      cx
         push      si

; ------ kontrola, zda jsou njak data

         xor       dx,dx                    ; stada kontrolnho soutu
         jcxz      KontrS9                  ; nejsou dn data

; ------ piten jednoho bajtu dat

KontrS1: mov       al,es:[si]               ; datov bajt k piten
         inc       si                       ; zven ukazatele dat
         mov       ah,8                     ; poet bit bajtu
KontrS2: shl       al,1                     ; jeden bit k piten
         adc       dx,0                     ; piten bitu ke stadai
         dec       ah                       ; ta bit
         jnz       KontrS2                  ; piten dalho bitu bajtu
         loop      KontrS1                  ; piten dalho bajtu

; ------ nvrat registr

KontrS9: pop       si
         pop       cx
         pop       ax
         ret

KontrS   ENDP

; -----------------------------------------------------------------------------
;        vysln vodu dat
; -----------------------------------------------------------------------------
; VSTUP: DI=poet bit vodnho tnu ("0") (22000=hlavika, 11000=data)
;        BH=poet bit synchronizace 1 ("1") (40=hlavika, 20=data)
;        BL=poet bit synchronizace 2 ("0") (40=hlavika, 20=data)
;        DS=datov segment
; VSTUP:CY=peruen programu
; -----------------------------------------------------------------------------

SaveUvod PROC      NEAR

; ------ schova registr

         push      ax
         push      bx
         push      di

; ------ vysln vodnho tnu

         or        di,di
         jz        SaveUv11                 ; nen dn bit
SaveUv1: clc                                ; vyle se bit 0
         call      SavBit                   ; vysln bitu 0
         jc        SaveUv9                  ; peruen programu
         dec       di                       ; ta bit vodnho tnu
         jnz       SaveUv1                  ; vysln dalho bitu

; ------ vysln synchronizace 1

SaveUv11:cmp       bh,0                     ; jsou njak bity ?
         je        SaveUv22                 ; nejsou dn bity
SaveUv2: stc                                ; vyle se bit 1
         call      SavBit                   ; vysln bitu 1
         dec       bh                       ; poet bit synchronizace
         jnz       SaveUv2                  ; vysln synchronizace 1

; ------ vysln synchronizace 2

SaveUv22:cmp       bl,0                     ; jsou njak bity ?
         je        SaveUv4                  ; nejsou dn bity
SaveUv3: clc                                ; vyle se bit 0
         call      SavBit                   ; vysln bitu 0
         dec       bl                       ; poet bit synchronizace
         jnz       SaveUv3                  ; vysln synchronizace 2

; ------ vysln startovacho bitu

SaveUv4: stc                                ; vyle se bit 1
         call      SavBit                   ; vysln bitu 1

; ------ nvrat registr

SaveUv9: pop       di
         pop       bx
         pop       ax
         ret

SaveUvod ENDP

; -----------------------------------------------------------------------------
;        vysln bloku dat
; -----------------------------------------------------------------------------
; VSTUP: ES:SI=adresa bloku dat
;        CX=poet bajt k vysln
;        DS=datov segment
; VSTUP:CY=peruen programu
; -----------------------------------------------------------------------------

SavBlok  PROC      NEAR

; ------ schova registr

         push      ax
         push      cx
         push      si

; ------ kontrola, zda jsou njak data k vysln

         clc
         jcxz      SavBlok9                 ; nejsou dn data

; ------ vysln bloku dat

         cld
SavBlok1:mov       al,es:[si]               ; bajt k vysln
         inc       si                       ; zven ukazatele
         call      SavByte                  ; vysln jednoho bajtu dat
         jc        SavBlok9                 ; peruen programu
         loop      SavBlok1                 ; vysln dalho bajtu

; ------ nvrat registr

SavBlok9:pop       si
         pop       cx
         pop       ax
         ret

SavBlok  ENDP

; -----------------------------------------------------------------------------
;        vysln jednoho bajtu
; -----------------------------------------------------------------------------
; VSTUP: AL=bajt k vysln
;        DS=datov segment
; VSTUP:CY=peruen
; ni registr AX !
; -----------------------------------------------------------------------------

SavByte  PROC      NEAR

         stc                                ; start bit "1"
         call      SavBit                   ; vysln startovacho bitu "1"
         shl       al,1
         call      SavBit                   ; bit 7
         shl       al,1
         call      SavBit                   ; bit 6
         shl       al,1
         call      SavBit                   ; bit 5
         shl       al,1
         call      SavBit                   ; bit 4
         shl       al,1
         call      SavBit                   ; bit 3
         shl       al,1
         call      SavBit                   ; bit 2
         shl       al,1
         call      SavBit                   ; bit 1
         shl       al,1
         call      SavBit                   ; bit 0
         ret

SavByte  ENDP

; -----------------------------------------------------------------------------
;        vysln jednoho bitu
; -----------------------------------------------------------------------------
; VSTUP: CY=bit je 1 (NC=bit je 0)
;        DS=datov segment
; VSTUP:CY=peruen
; ni registr AH !
; -----------------------------------------------------------------------------

SavBit   PROC      NEAR

; ------ stanoven dlky impulsu

         mov       ah,ds:[DelImp]           ; dlka impulsu "0"
         jnc       SavBit1                  ; je bit 0
         shl       ah,1                     ; jinak impuls "1"

; ------ vysln dvou impuls

SavBit1: call      SavImp                   ; vysln impulsu "+"
         call      SavImp                   ; vysln impulsu "-"
         ret

SavBit   ENDP

; -----------------------------------------------------------------------------
;        zahjen vysln jednoho impulsu
; -----------------------------------------------------------------------------
; VSTUP: AH=dlka impulsu
;        DS=datov segment
; VSTUP:CY=peruen programu
; -----------------------------------------------------------------------------

SavImp   PROC      NEAR

; ------ test, zda je peruen porgramu

         sti                                ; peruen mus bt povoleno
SavImp1: test      byte ptr ds:[Param],bit0 ; je peruen programu ?
         stc                                ; pznak peruen
         jnz       SavImp9                  ; je peruen programu

; ------ ekn na ukonen jednoho impulsu

         cmp       byte ptr ds:[CitImp],0   ; je ji cel impuls ?
         jne       SavImp1                  ; ekn na ukonen impulsu

; ------ zatek novho impulsu

         mov       ds:[CitImp],ah           ; dlka novho impulsu
                                            ; zde je pznak NC
SavImp9: ret

SavImp   ENDP

