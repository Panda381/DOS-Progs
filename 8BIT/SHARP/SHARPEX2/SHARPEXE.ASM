Start62:

; ------ parametry zavad؟e

         push      ds
         pop       es
         mov       si,offset HlavaDel
         mov       di,offset LoadHlav
         mov       cx,13
         cld
         rep       movsb                    ; penos parametr souboru

         mov       si,offset HlavaNam
         mov       di,offset LoadNam
         mov       cx,17
