#include "..\Include.h"

///////////////////////////////////////////////////////////////////////////////
// constructor

CLock::CLock()
{
	::InitializeCriticalSection(&m_CriticalSection);
}

///////////////////////////////////////////////////////////////////////////////
// destructor

CLock::~CLock()
{
	::DeleteCriticalSection(&m_CriticalSection);
}

///////////////////////////////////////////////////////////////////////////////
// lock

void CLock::Lock()
{
	::EnterCriticalSection(&m_CriticalSection);
}

///////////////////////////////////////////////////////////////////////////////
// unlock

void CLock::Unlock()
{
	::LeaveCriticalSection(&m_CriticalSection);
}
