// map font table

void FontTabMap()
{
	int i, j, x, x2, n;
	const u8* b;

	// loop through all characters
	x2 = 0;
	for (i = FONTMIN; i <= FONTMAX; i++)
	{
		// get width of one character
		x = x2;
		do {

			// get number of valid pixels in one column
			b = &Font11->d[x2++];
			n = 0;
			for (j = Font11->h; j > 0; j--)
			{
				if (*b < 128) n++;
				b += Font11->w;
			}
		} while (n > 0);

		// set character position and width
		FontTabX[i - FONTMIN] = x;
		FontTabW[i - FONTMIN] = x2 - x;
	}
}
